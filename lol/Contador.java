public class Contador {
 
  private int cont = 0;
      
      public void Reiniciar(){
          this.cont = 0;
        }
        
      public int obtCont(){
          return this.cont;
        }  
      
      public void Incr() {
          cont = cont+1;
        }
    
    }

   
    

